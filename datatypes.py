def main():
    print_strings()
    print_integers()
    print_floats()
    print_long()
    print_booleans()

def print_strings():
    my_string = "Hello, world!"
    print("String:", my_string)

def print_integers():
    my_integer = 42
    print("Integer:", my_integer)

def print_floats():
    my_float = 3.14
    print("Float:", my_float)

def print_long():
    my_long = 12345678901234567890
    print("Long:", my_long)

def print_booleans():
    my_boolean = True
    print("Boolean:", my_boolean)

if __name__ == "__main__":
    main()
