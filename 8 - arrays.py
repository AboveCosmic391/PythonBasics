# Example 1: Creating a list of integers
numbers = [1, 2, 3, 4, 5]
print("List of numbers:", numbers)

# Example 2: Creating a list of strings
fruits = ["apple", "banana", "orange", "grape", "kiwi"]
print("List of fruits:", fruits)

# Example 3: Creating a list of mixed data types
mixed_list = [1, "apple", True, 3.14]
print("Mixed list:", mixed_list)

# Example 4: Accessing elements of a list
print("First fruit:", fruits[0])
print("Second number:", numbers[1])

# Example 5: Modifying elements of a list
fruits[2] = "strawberry"
print("Modified list of fruits:", fruits)

# Example 6: Adding elements to a list
fruits.append("pineapple")
print("List of fruits after appending:", fruits)

# Example 7: Removing elements from a list
removed_fruit = fruits.pop(2)
print("Removed fruit:", removed_fruit)
print("List of fruits after removing:", fruits)
