def demonstrate_string():
    # String demonstration
    my_string = "Hello, World!"
    print("String value:", my_string)

def demonstrate_integer():
    # Integer demonstration
    my_integer = 42
    print("Integer value:", my_integer)

def demonstrate_float():
    # Float demonstration
    my_float = 3.14
    print("Float value:", my_float)

def demonstrate_boolean():
    # Boolean demonstration
    my_bool = True
    print("Boolean value:", my_bool)

def demonstrate_long():
    # Long demonstration
    my_long = 1234567890123456789012345678901234567890
    print("Long value:", my_long)

def main():
    # Main function to call other functions
    print("Demonstrating Basic Data Types:")
    demonstrate_string()
    demonstrate_integer()
    demonstrate_float()
    demonstrate_boolean()
    demonstrate_long()

if __name__ == "__main__":
    main()
